package si.um.ii.swrools.exceptions;

public class GenerationException extends Exception {

	/**
	 * Generated version ID
	 */
	private static final long serialVersionUID = 7817292158193867761L;

	public GenerationException() {
	}

	public GenerationException(String arg0) {
		super(arg0);
	}

	public GenerationException(Throwable arg0) {
		super(arg0);
	}

	public GenerationException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public GenerationException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

}
